public class Memoria_Valor {
        private int posicao;
        private int valor;
    
        public Memoria_Valor(int posicao, int valor) {
            this.posicao = posicao;
            this.valor = valor;
        }
    
        public int getPosicao() {
            return posicao;
        }
        public void setPosicao(int posicao) {
            this.posicao = posicao;
        }
        public int getValor() {
            return valor;
        }
        public void setValor(int valor) {
            this.valor = valor;
        }
    
}
