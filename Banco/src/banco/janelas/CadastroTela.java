    private void CadastroBTActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CadastroBTActionPerformed
        String nome = NomeTX.getText();
        String senha = String.valueOf(SenhaTX.getText());
        double saldo = Integer.parseInt(SaldoTX.getText());

        try {
            Cadastro cadastro = new Cadastro(nome, senha, saldo);
            cadastro.conta();
            dispose();
            JOptionPane.showMessageDialog(null, "Conta Criada! \n Sua Conta Ã©: " + cadastro.getConta());

        } catch (IOException e) {
            e.printStackTrace();
        }
        Main run = new Main();
        run.setVisible(true);
    }//GEN-LAST:event_CadastroBTActionPerformed

